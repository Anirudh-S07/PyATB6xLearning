# Escape Sequence
# \n -> new line
# \t -> tab
# \b -> backspace (1 char backspace)

"""
| Escape | Description        |
| ------ | ------------------ |
| `\n`   | Newline            |
| `\t`   | Tab                |
| `\\`   | Backslash          |
| `\'`   | Single quote       |
| `\"`   | Double quote       |
| `\a`   | Bell (Alert sound) |
"""

print("Hello\nWorld")
print("Hello\tWorld")
print("Hello\bWorld")

